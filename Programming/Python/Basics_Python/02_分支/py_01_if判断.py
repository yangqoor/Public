# 判断年龄示例程序
# 定义年龄变量
# ctrl+/ 批量修改是否注释
# 输入年龄
age = int(input("请输入年龄："))
# age = 18
# 判断输入是否合理年龄在0-120之间
# age >= 0
# age <= 120
if 0 <= age <= 120:
    print("年龄输入正确")
    # 判断年龄
    """
    if else 及带缩进的语句，可看做同一代码块
    if 条件:
        语句1
    else:
        语句2
    """
    if age >= 18:
        print("您已经成年！")
        print("欢迎光临！")
    else:
        print("你还小，老实儿的！")
        print("回去学习～")

print("%%%%%%%%%%%%% 执行定位 %%%%%%%%%%%%%")
