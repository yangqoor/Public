%--------------------------------------------------------------------------
%   out = rotate_2d(thetad)
%--------------------------------------------------------------------------
%   功能：
%   二维向量旋转矩阵,对列向量左乘进行旋转操作
%--------------------------------------------------------------------------
%   输入：
%           thetad              二维平面旋转角度
%   输出：
%           out                 旋转矩阵
%--------------------------------------------------------------------------
%   例子：
%   rotate_2d(45)*[1 0]'
%--------------------------------------------------------------------------
function out = rotate_2d(thetad)
out = [cosd(thetad) -sind(thetad);sind(thetad) cosd(thetad)];
end
