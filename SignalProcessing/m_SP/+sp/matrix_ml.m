%--------------------------------------------------------------------------
%   A = matrix_ml(M,L)
%--------------------------------------------------------------------------
%   功能：
%   频率因子阵，矩阵形式的傅里叶变换,一般最小二乘时候使用
%--------------------------------------------------------------------------
%   输入:
%           M           fft的点数
%           L           滤波器的抽头数
%           A           输出矩阵A,即为傅里叶变换的矩阵形式
%--------------------------------------------------------------------------
%   例子：
%   sig (L*1)
%   sig_freq = matrix(M,L)*sig;
%--------------------------------------------------------------------------
function A = matrix_ml(M,L)
if M < L
    disp('注意:只有M>=L的时候等效于傅里叶变换')
end
[l,m] = meshgrid((0:L-1),(0:M-1));
A = exp(-1j.*2.*pi.*m.*l./M);
end

