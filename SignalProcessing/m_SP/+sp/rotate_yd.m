%--------------------------------------------------------------------------
%   yaw = rotate_yd(thetad)
%--------------------------------------------------------------------------
%   功能：
%   绕Y方向的旋转矩阵
%--------------------------------------------------------------------------
%   输入：
%           theta               角度
%   输出：
%           yaw                 旋转矩阵
%--------------------------------------------------------------------------
%	例子：
%	rotate_yd(30)*[x;y;z]
%--------------------------------------------------------------------------
function yaw = rotate_yd(thetad)
yaw = [cosd(thetad) 0 sind(thetad);0 1 0;-sind(thetad) 0 cosd(thetad)];
end