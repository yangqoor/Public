%--------------------------------------------------------------------------
%   vpp2dbm(Vpp,R)
%--------------------------------------------------------------------------
%   功能:
%   电压转换为dbm
%--------------------------------------------------------------------------
%   输入:
%           Vpp                 信号峰峰值电压
%           R                   阻抗匹配(默认=50Ω)
%   输出:
%           dbm
%--------------------------------------------------------------------------
%   例子:
%   vpp2dbm(1)
%   ans =3.9794
%   vpp2dbm(1,1e6)
%   ans = -39.0309
%--------------------------------------------------------------------------
function dBm = vpp2dbm(Vpp,R)
if nargin==1
    R=50;
end
dBm = 20*log10(Vpp/2)-10*log10(2*R)+30;
end