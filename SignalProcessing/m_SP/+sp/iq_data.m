%--------------------------------------------------------------------------
%   [I,Q] = iq_data(long)
%--------------------------------------------------------------------------
%   功能：
%   按照1/4 生成IQ数据
%   I路：[1 0 -1 0]
%   Q路：[0 1 0 -1]
%--------------------------------------------------------------------------
%   输入：
%           long            生成数据长度
%   输出：
%           I               实部数据
%           Q               虚部数据
%--------------------------------------------------------------------------
%   例子：
%   [i,q] = iq_data(4)
% i =
%      1
%      0
%     -1
%      0
% q =
%      0
%      1
%      0
%     -1
%--------------------------------------------------------------------------
function [I,Q] = iq_data(long)
N = fix(long/4);                %取整
M = rem(long,4);                %取余数
I(1,:)= ones(1,N+1);
I(2,:)= zeros(1,N+1);
I(3,:)= -1*ones(1,N+1);
I(4,:)= zeros(1,N+1);

Q(1,:)= zeros(1,N+1);
Q(2,:)= ones(1,N+1);
Q(3,:)= zeros(1,N+1);
Q(4,:)= -1*ones(1,N+1);

I = reshape(I,1,4*(N+1));
Q = reshape(Q,1,4*(N+1));
I = I(1:(4*N+M))';
Q = Q(1:(4*N+M))';
end