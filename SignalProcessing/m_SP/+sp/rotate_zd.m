%--------------------------------------------------------------------------
%   yaw = rotate_zd(thetad)
%--------------------------------------------------------------------------
%   功能：
%   绕Z方向的旋转矩阵
%--------------------------------------------------------------------------
%   输入：
%           theta               角度
%   输出：
%           yaw                 旋转矩阵
%--------------------------------------------------------------------------
%	例子：
%	rotate_zd(30)*[x;y;z]
%--------------------------------------------------------------------------
function yaw = rotate_zd(thetad)
yaw = [cosd(thetad) -sind(thetad) 0;sind(thetad) cosd(thetad) 0;0 0 1];
end