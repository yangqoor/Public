clear;clc;

%--------------------------------------------------------------------------
%   用随机数验证傅里叶变换域频率因子阵是否为等效关系
%--------------------------------------------------------------------------
rng(2021);
L = 101;
M = 100;
sig = randn(L,1);
A = rt.matrix_ml(M,L);

sig_mf = A*sig;

sig_f = fft(sig,M,1);

rt.p3(sig_mf);hold on
rt.p3(sig_f,'--');hold off