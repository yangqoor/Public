%--------------------------------------------------------------------------
%   用quantizer生成一个数据转换器
%   并设置其 mode 为 float有符号定点模式 支持自定义
%   自定义格式 format 为[wordlength exponentlength] 第一个是总长度
%--------------------------------------------------------------------------
q = quantizer('mode','float','format',[16 6]);

%--------------------------------------------------------------------------
%   数据转换，调用数据转换对象q
%--------------------------------------------------------------------------
bin2num(q,'0110100000000000')
hex2num(q,'6800')