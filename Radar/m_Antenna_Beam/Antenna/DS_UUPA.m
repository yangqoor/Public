    I=(1-P);%complementary set of the initial DS

    S1=zeros(size(u));
    for m=1:M
        for n=1:N
            S1=S1+I(m,n)*exp(i*k(s)*((n-1)*d*u+(m-1)*d*v));
        end 
    end
    S1=abs(S1);
    S1=S1/max(max(S1));
    S1=20*log10(S1);
    Mrsl=zeros(1,(res-1)/2);
    for m=1:(res-1)/2
        for n=1:(res-1)/2
            if S1(m,n)>S1(m,n+1),brk=n;end
        end
        Mrsl(m)=max(S1(m,1:brk));
    end
    mrsl(s)=max(Mrsl);
    for n=1:(res-1)/2
        if S1(1,n)>S1(1,n+1),brk=n;end
    end
    nnbw(s)=2*180*(0-theta(brk))/pi;
    s
end
