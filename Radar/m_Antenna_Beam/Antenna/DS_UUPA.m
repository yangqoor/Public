for s=1:length(B)
    Ds=mod(D+40,V);
    P=zeros(M,N);
    I=zeros(M,N);
    for m=1:K
        P(mod(Ds(m),M)+1,mod(Ds(m),N)+1)=1;%经计算 对于D(63,31,15)sh=3,8,12,17,21,26,30,35,39,44,48,53,57,62msll<-10.8dB
